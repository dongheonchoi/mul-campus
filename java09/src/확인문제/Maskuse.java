package 확인문제;

public class Maskuse {
	public String color;
	public int cost;
	public int num;
	
	



	public Maskuse(String color, int cost, int num) {
		super();
		this.color = color;
		this.cost = cost;
		this.num = num;
	}
	
	
	@Override
	public String toString() {
		return "Maskuse [color=" + color + ", cost=" + cost + ", num=" + num + "]";
	}
	
	
	
}
