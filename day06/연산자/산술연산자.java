package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		// 산술연산자
		int x = 20;
		int y = 10;
		
		System.out.println("더한값은 " + (x+y)); //계산 순서
		System.out.println("뺀값은 " + (x-y));
		System.out.println("곱한 값은 " + x*y);
		System.out.println("나눈 값은 " + x/y);
		System.out.println("나머지값은" + x%y);
		
	}

}
