package 조건문;

public class if문1 {

	public static void main(String[] args) {
		int x = 0;
		
		
		if (x == 0) {
			System.out.println("이게 실행 됩니다.");
		}
		else {//false 일때 처리하고 싶은 내용을 쓰면 된다.
			System.out.println("false일때 실행이 됩니다.");
		}
			
		if (x == 1) {
			System.out.println("이건 안되겠죠?");
		}
	
	
	}

}
